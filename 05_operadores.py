# Operadores numericos
#
a = 10
b = 5
print(a + b)  # Suma
print(a - b)  # Resta
print(a * b)  # Multiplicacion
print(a / b)  # Division
print(a % b)  # Modulo
print(a**b)  # Exponente
print(a // b)  # Division entera

print(a > b)  # Mayor que
print(a < b)  # Menor que
print(a == b)  # Igual que
print(a != b)  # Diferente que
print(a >= b)  # Mayor o igual que
print(a <= b)  # Menor o igual que
